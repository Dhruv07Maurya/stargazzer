// Helper to get color by group
export const getGroupColor = (group: string) => {
    switch (group) {
        case 'Station': return '#ff3333'; // Red
        case 'Starlink': return '#33ff33'; // Green
        case 'GPS': return '#3333ff'; // Blue
        case 'Weather': return '#ffff33'; // Yellow
        default: return '#ffffff';
    }
};
